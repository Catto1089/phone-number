// ฟังก์ชันสร้าง UI และคัดกรองข้อมูล (แก้ไข Logic อุดรอยรั่วแล้ว)
function renderOfficialStructure(searchFilter = "") {
    const container = document.getElementById('categoryContainer');
    if (!container) return;
    container.innerHTML = '';
    
    let hasGlobalData = false;
    const searchString = searchFilter.toLowerCase().trim();

    for (let key in officialDatabase) {
        const cat = officialDatabase[key];
        
        const filteredList = cat.list.filter(item => {
            // ถ้าช่องค้นหาว่าง ให้คืนค่าแสดงผลทั้งหมดทันที
            if (searchString === "") return true;

            // 1. ค้นหาจากข้อความตัวอักษร (ชื่อ, คำอธิบาย, คีย์เวิร์ด)
            const matchText = item.name.toLowerCase().includes(searchString) || 
                              item.desc.toLowerCase().includes(searchString) ||
                              item.keys.toLowerCase().includes(searchString);

            // 2. ค้นหาจากเบอร์โทรศัพท์ (ดึงเฉพาะตัวเลขมาส่อง)
            const cleanSearch = searchString.replace(/[^0-9]/g, '');
            const cleanPhone = item.phone.replace(/[^0-9]/g, '');
            
            // แก้บั๊กสำคัญ: จะเช็คเบอร์โทรศัพท์ก็ต่อเมื่อในช่องค้นหามีตัวเลขโผล่มาเท่านั้น!
            const matchPhone = cleanSearch !== '' && cleanPhone.includes(cleanSearch);

            return matchText || matchPhone;
        });

        if (filteredList.length > 0) {
            hasGlobalData = true;
            const section = document.createElement('section');
            section.className = "mb-8 animate-fadeIn";
            
            let tableRows = "";
            filteredList.forEach(item => {
                tableRows += `
                    <tr class="border-b border-slate-200 hover:bg-slate-50 transition-colors">
                        <td class="px-4 py-4 font-semibold text-slate-800 w-1/4 col-name">${item.name}</td>
                        <td class="px-4 py-4 font-mono font-bold text-blue-800 w-1/6 text-center col-phone">${item.phone}</td>
                        <td class="px-4 py-4 text-slate-500 w-2/5 font-light col-desc">${item.desc}</td>
                        <td class="px-4 py-4 text-right w-1/6">
                            <a href="tel:${item.phone}" class="btn-call">
                                <i class="fa-solid fa-phone text-xs"></i> โทรออก
                            </a>
                        </td>
                    </tr>
                `;
            });

            section.innerHTML = `
                <div class="flex items-center gap-2 mb-4">
                    <div class="p-2 bg-slate-800 text-white rounded">
                        <i class="fa-solid ${cat.icon} text-sm"></i>
                    </div>
                    <h2 class="text-lg font-bold text-slate-800">${cat.title}</h2>
                </div>
                <div class="table-container overflow-x-auto">
                    <table class="w-full text-left border-collapse min-w-[600px]">
                        <thead>
                            <tr class="table-header text-slate-700 text-xs uppercase tracking-wider">
                                <th class="px-4 py-3 font-semibold">ชื่อหน่วยงาน / ส่วนราชการ</th>
                                <th class="px-4 py-3 font-semibold text-center">หมายเลขสายด่วน</th>
                                <th class="px-4 py-3 font-semibold">ขอบเขตการให้บริการ</th>
                                <th class="px-4 py-3 text-right font-semibold">คำสั่ง</th>
                            </tr>
                        </thead>
                        <tbody>
                            ${tableRows}
                        </tbody>
                    </table>
                </div>
            `;
            container.appendChild(section);
        }
    }

    // จัดการแสดงผลตัวเตือน "ไม่พบข้อมูล"
    const noResult = document.getElementById('noResult');
    if (noResult) {
        if (hasGlobalData) {
            noResult.classList.add('hidden');
        } else {
            noResult.classList.remove('hidden');
        }
    }
}

// ฟังก์ชันปรับขนาดตัวอักษรหลักของหน้าเว็บ
function changeFontSize(size) {
    const body = document.body;
    body.classList.remove('text-small', 'text-normal', 'text-large');
    
    if (size === 'small') body.classList.add('text-small');
    if (size === 'normal') body.classList.add('text-normal');
    if (size === 'large') body.classList.add('text-large');
    
    localStorage.setItem('preferredFontSize', size);
    updateActiveButton(size);
}

function updateActiveButton(activeSize) {
    const buttons = document.querySelectorAll('[onclick^="changeFontSize"]');
    buttons.forEach(btn => {
        if (btn.getAttribute('onclick').includes(activeSize)) {
            btn.className = "bg-slate-700 text-white px-2 py-0.5 rounded transition-colors font-bold";
        } else {
            btn.className = "hover:text-white px-1.5 py-0.5 rounded transition-colors font-medium";
        }
    });
}

// ผูกระบบ Event ค้นหา
const searchInput = document.getElementById('searchInput');
if (searchInput) {
    searchInput.addEventListener('input', function(e) {
        renderOfficialStructure(e.target.value);
    });
}

// โหลดข้อมูลขึ้นหน้าจอเมื่อโหลดหน้าเว็บเสร็จสิ้น
document.addEventListener('DOMContentLoaded', () => {
    const savedSize = localStorage.getItem('preferredFontSize') || 'normal';
    changeFontSize(savedSize);
    renderOfficialStructure();
});
